        <h3 id="bath">Public Baths</h3>
        <table border="0" cellpadding="5">
            <tr>
                <td><img src="images/bath1.jpg" alt="future bath" width="250" height="180"></td>
                <td><p>In the past, many homes in Japan were not equipped with a bathtub. To fill this void,
                        the neighborhood sento or public bath was a place where the locals could
                        go to wash themselves, soak in a tub and socialize with neighbors.

                        Some sento, typically in hot spring resort towns, utilize natural hot spring water for their
                        baths. In this case they are considered an onsen bath. Public baths that are not supplied by hot
                        spring water, use heated tap water instead. With the exception of some theme park style bath
                        complexes, public baths are segregated by gender and swimsuits are not worn.</p>
                </td>
            </tr>
