        <h3 id="festival">Japanese Festivals (Matsuri)</h3>
        <table border="0" cellpadding="5">
            <tr>
                <td><img src="images/mikoshi.jpg" alt="festival" width="250" height="200"></td>
                <td><p> Most of the festivals are held annually and celebrate the shrine's deity or a seasonal
                        or historical event. Some festival are held over several days.

                        An important element of Japanese festivals are processions, in which the local shrine's kami
                        (Shinto deity) is carried through the town in mikoshi (palanquins). It is the only time of the
                        year when the kami leaves the shrine to be carried around town.

                        Many festivals also feature decorated floats (dashi), which are pulled through the town,
                        accompanied by drum and flute music by the people sitting on the floats. Every festival has its
                        own characteristics. While some festivals are calm and meditative, many are energetic and
                        noisy.</p>
                </td>
            </tr>
        </table>
